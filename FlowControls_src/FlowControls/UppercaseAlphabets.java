package FlowControls;

public class UppercaseAlphabets {
    public static void main(String[] args) {
        System.out.println("Uppercase Alphabets from A to Z:");

        // Loop through characters from 'A' to 'Z'
        for (char ch = 'A'; ch <= 'Z'; ch++) {
            System.out.print(ch + " ");
        }
    }
}


