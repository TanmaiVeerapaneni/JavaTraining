package FlowControls;

public class AlphabetPrinter {
	    public static void main(String[] args) {
	        System.out.println("Alphabets from a to z:");

	        // Loop through ASCII values of 'a' to 'z'
	        for (char ch = 'a'; ch <= 'z'; ch++) {
	            System.out.print(ch + " ");
	        }
	    }
	}

